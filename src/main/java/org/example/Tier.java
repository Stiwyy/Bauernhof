package org.example;

public abstract class Tier {
    private String name;
    private int age;

    protected Tier(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public abstract void speak();

    public abstract int move();
}
